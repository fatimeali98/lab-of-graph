import turtle
d=turtle.Turtle()
turtle.bgcolor("red")
d.up()
d.goto(0,-100)
d.down()
d.begin_fill()
d.fillcolor("yellow")
d.circle(120)
d.end_fill()
d.up()
d.goto(-67,-40)
d.setheading(-60)
d.width(10)
d.down()
d.circle(80,120)
d.fillcolor("black")
for i in range(-35,105,70):
    d.up()
    d.goto(i,35)
    d.setheading(0)
    d.down()
    d.begin_fill()
    d.circle(15)
    d.end_fill()
d.up()
d.goto(0,-20)
d.color("black")
d.down()
d.goto(20,20)
turtle.done()